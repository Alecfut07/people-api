class Employee {
    constructor(user, district, address, type, name, age, cellphone, birthdate, email, salary) {
        this.user = user;
        this.district = district;
        this.address = address;
        this.type = type;
        this.name = name;
        this.age = age;
        this.cellphone = cellphone;
        this.birthdate = birthdate;
        this.email = email;
        this.salary = salary;
    }
}

module.exports = Employee;
